//! A collection of node-specific RPC methods.
//! Substrate provides the `sc-rpc` crate, which defines the core RPC layer
//! used by Substrate nodes. This file extends those RPC definitions with
//! capabilities that are specific to this project's runtime configuration.

import { toNano } from '@ton/core';
import { TonApp } from '../wrappers/TonApp';
import { NetworkProvider } from '@ton/blueprint';
import { Channel } from '../wrappers/Channel';

export async function run(provider: NetworkProvider) {
    const channel = provider.open(await Channel.fromInit(provider.sender().address!));
    const tonApp = provider.open(await TonApp.fromInit(channel.address));

    await channel.send(
        provider.sender(),
        {
            value: toNano('0.05'),
        },
        {
            $$type: 'Deploy',
            queryId: 1n,
        }
    );

    await provider.waitForDeploy(channel.address, 20);

    await tonApp.send(
        provider.sender(),
        {
            value: toNano('0.05'),
        },
        {
            $$type: 'Deploy',
            queryId: 1n,
        }
    );

    await provider.waitForDeploy(tonApp.address, 20);

    await channel.send(
        provider.sender(),
        {
            value: toNano(0.2),
        },
        {
            $$type: 'RegisterApp',
            app: tonApp.address
        }
    );
    console.log({
        tonApp: tonApp.address,
        channel: channel.address
    });

    // run methods on `soraApp`
}
